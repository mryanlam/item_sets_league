    <hr>
    <b>Gold Efficiency</b><br>
    <ul>
        <li>Blade of the Ruined King is 89.1% gold efficient, without its active.</li>
        <li>Blade of the Ruined King's gold efficiency is further increased by 5% for every 100 of the target's current health left (excluding the last 125 of the target's current health, because 10 on-hit damage is the minimum damage done).</li>
        <li>For the item to be considered 100% gold efficient, the passive and active would need to have a combined gold value of 350.</li>
    </ul>
